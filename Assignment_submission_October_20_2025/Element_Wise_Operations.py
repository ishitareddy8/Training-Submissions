# Importing the NumPy library
import numpy as np

# Create two arrays
arr1 = np.array([30, 25, 58, 46, 64])
arr2 = np.array([4, 8, 7, 26, 14])

# Perform element-wise operations
print("Array 1:", arr1)
print("Array 2:", arr2)

# Addition
print("\nElement-wise Addition:", arr1 + arr2)

# Subtraction
print("Element-wise Subtraction:", arr1 - arr2)

# Multiplication
print("Element-wise Multiplication:", arr1 * arr2)

# Division
print("Element-wise Division:", arr1 / arr2)

# Exponentiation
print("Element-wise Power:", arr1 ** arr2)

# Modulus
print("Element-wise Modulus:", arr1 % arr2)